let currentSymbolicLabels = null;

function isPermutationGroup(key) {
    // S4, A4, S5, A5, S6 etc. stay as perms
    if ((key.startsWith('S') || key.startsWith('A')) && /^\d/.test(key.substring(1))) {
        return true;
    }
    return false;
}

function getSymbolicLabels(elements, table, genObjs) {
    const n = elements.length;
    const labels = new Array(n).fill(null);
    const words = new Array(n).fill(null);

    // Identify if Abelian to sort words
    const isAbelian = checkIfAbelian(table);

    labels[0] = "e"; // Use plain 'e' for dropdown compatibility
    words[0] = [];

    const queue = [0];
    const visited = new Set([0]);

    let head = 0;
    while (head < queue.length) {
        const u = queue[head++];
        genObjs.forEach((gen, gIdx) => {
            const v = table[gIdx][u];
            if (!visited.has(v)) {
                visited.add(v);
                words[v] = [...words[u], gIdx];
                queue.push(v);
            }
        });
    }

    for (let i = 1; i < n; i++) {
        let word = words[i];
        if (isAbelian) {
            word = [...word].sort((a, b) => a - b);
        }
        labels[i] = formatWord(word, genObjs);
    }
    return labels;
}

function checkIfAbelian(table) {
    const numGens = table.length;
    if (numGens < 2) return true;
    const numNodes = table[0].length;
    for (let i = 0; i < numGens; i++) {
        for (let j = i + 1; j < numGens; j++) {
            for (let k = 0; k < numNodes; k++) {
                if (table[i][table[j][k]] !== table[j][table[i][k]]) return false;
            }
        }
    }
    return true;
}

function formatWord(word, genObjs) {
    if (!word || word.length === 0) return "e";

    const parts = [];
    let currentGen = word[0];
    let count = 1;

    for (let i = 1; i <= word.length; i++) {
        if (i < word.length && word[i] === currentGen) {
            count++;
        } else {
            const label = genObjs[currentGen].label;
            if (count === 1) parts.push(label);
            else parts.push(`${label}${toSuperscript(count)}`);
            if (i < word.length) {
                currentGen = word[i];
                count = 1;
            }
        }
    }
    return parts.join('');
}





/**
 * If the group is a dihedral group D_n, returns an array of canonical labels
 * in the standard order: e, r, r², …, r^(n-1), s, rs, …, r^(n-1)s
 * where genObjs[0] is the rotation and genObjs[1] is the reflection.
 * Returns null if the group is not dihedral (under this generator ordering).
 *
 * table[gIdx][elemIdx] = index of (elem * gen[gIdx])  (right-multiplication)
 */
function getDihedralLabels(elements, table, genObjs) {
    const size = elements.length;
    if (genObjs.length !== 2 || size % 2 !== 0) return null;
    const n = size / 2;
    if (n < 2) return null;

    // Build powers of r: rPowers[k] = element index of r^k
    const rPowers = [0];
    let curr = table[0][0]; // identity * r = r
    for (let k = 1; k < n; k++) {
        rPowers.push(curr);
        curr = table[0][curr];
    }
    // Verify r has order exactly n
    if (curr !== 0) return null;

    const sElem = table[1][0]; // identity * s = s
    // Verify s² = e
    if (table[1][sElem] !== 0) return null;

    // Verify dihedral relation: s·r·s = r^{-1} = r^(n-1)
    const srElem  = table[0][sElem];  // s * r
    const srsElem = table[1][srElem]; // s * r * s
    if (srsElem !== rPowers[n - 1]) return null;

    // Build canonical labels indexed by element index
    const labels = new Array(size).fill(null);
    for (let k = 0; k < n; k++) {
        labels[rPowers[k]] = formatWord(new Array(k).fill(0), genObjs);
    }
    for (let k = 0; k < n; k++) {
        labels[table[1][rPowers[k]]] = formatWord([...new Array(k).fill(0), 1], genObjs);
    }
    return labels;
}

/**
 * Returns an array of indices into `elements` in the canonical dihedral order:
 * e, r, r², …, r^{n-1}, s, rs, …, r^{n-1}s
 * Returns null if the group is not dihedral (same validation as getDihedralLabels).
 */
function getDihedralOrder(elements, table, genObjs) {
    const size = elements.length;
    if (genObjs.length !== 2 || size % 2 !== 0) return null;
    const n = size / 2;
    if (n < 2) return null;

    const rPowers = [0];
    let curr = table[0][0];
    for (let k = 1; k < n; k++) {
        rPowers.push(curr);
        curr = table[0][curr];
    }
    if (curr !== 0) return null;

    const sElem = table[1][0];
    if (table[1][sElem] !== 0) return null;

    const srElem  = table[0][sElem];
    const srsElem = table[1][srElem];
    if (srsElem !== rPowers[n - 1]) return null;

    const order = [];
    for (let k = 0; k < n; k++) order.push(rPowers[k]);
    for (let k = 0; k < n; k++) order.push(table[1][rPowers[k]]);
    return order;
}

// Helper to compute conjugacy classes
function computeConjugacyClasses(elements) {
    const N = elements.length;
    const classes = [];
    const visited = new Array(N).fill(false);

    // Map elements to their string representation for quick lookup
    const elementToString = new Map(elements.map(e => [e.join(','), e]));
    const stringToElementIndex = new Map(elements.map((e, i) => [e.join(','), i]));

    for (let i = 0; i < N; i++) {
        if (visited[i]) continue;

        const currentClass = [];
        const x = elements[i]; // The representative of this class

        for (let j = 0; j < N; j++) {
            const g = elements[j];
            const g_inv = CayleyGraph.invertPerm(g);

            // Compute g * x * g_inv
            const gx = CayleyGraph.composePerm(g, x);
            const conjugate = CayleyGraph.composePerm(gx, g_inv);

            const conjugateStr = conjugate.join(',');
            const conjugateIdx = stringToElementIndex.get(conjugateStr);

            if (conjugateIdx !== undefined && !visited[conjugateIdx]) {
                currentClass.push(conjugateIdx);
                visited[conjugateIdx] = true;
            }
        }
        classes.push(currentClass);
    }
    return classes;
}

function generateHSLColors(count) {
    const colors = [];
    for (let i = 0; i < count; i++) {
        // Space out hues evenly
        const hue = (i * 360) / count;
        // Vary saturation and lightness slightly for even more distinction if count is high
        const saturation = 70 + (i % 2) * 20; // Toggle 70% and 90%
        const lightness = 50 + (i % 3) * 10;   // Variations in lightness
        colors.push(`hsl(${hue}, ${saturation}%, ${lightness}%)`);
    }
    return colors;
}

function updateConjugacyLegend(classes, elements) {
    const legendEl = document.getElementById('conjugacy-legend');
    if (!legendEl) return;

    legendEl.innerHTML = '';

    // Check if Abelian
    let isAbelian = true;
    for (let i = 0; i < elements.length; i++) {
        const classIdx = classes.findIndex(cls => cls.includes(i));
        if (classes[classIdx].length > 1) {
            isAbelian = false;
            break;
        }
    }

    if (isAbelian) {
        legendEl.innerHTML = '<div style="font-style: italic; color: #94a3b8;">Abelian Group: Each element is in its own conjugacy class.</div>';
        return;
    }

    const colors = generateHSLColors(classes.length);
    classes.forEach((currentClass, idx) => {
        const item = document.createElement('div');
        item.style.display = 'flex';
        item.style.alignItems = 'center';
        item.style.gap = '8px';
        item.style.marginBottom = '4px';

        const colorBox = document.createElement('div');
        colorBox.style.width = '12px';
        colorBox.style.height = '12px';
        colorBox.style.borderRadius = '2px';
        colorBox.style.backgroundColor = idx === 0 ? '#e2e8f0' : colors[idx];

        const text = document.createElement('span');
        const repIdx = currentClass[0];
        const repStr = currentSymbolicLabels
            ? currentSymbolicLabels[repIdx]
            : CayleyGraph.permToCycleString(elements[repIdx]);
        text.innerHTML = `Size ${currentClass.length}: <b>${repStr}</b>`;

        item.appendChild(colorBox);
        item.appendChild(text);
        legendEl.appendChild(item);
    });
}

